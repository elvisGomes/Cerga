            <h4>Que dit la loi ?</h4>
            
            <p>Le bénéficiaire du RSA ayant fait l'objet d'une orientation sociale conclut avec le département,
            représenté par le Président du Département du Nord, sous un délai de 2 mois, un contrat librement
            débattu énumérant leurs engagements réciproques en matière d'insertion sociale ou professionnelle.
            Le Département peut, par convention, confier la conclusion de ce contrat à une collectivité territoriale,
            à un groupement de collectivités territoriales, à une association, à la CAF ou à la MSA.</p>                                                 
            <p>Article L.262-36 du Code de l'Action Sociale et des Familles</p>
            
            <h4>Qu'est-ce qu'un contrat d'engagements réciproques ?</h4>
            
            <p>Un contrat d'engagements réciproques définit le mode d'accompagnement approprié en vue de la réalisation
            du projet d'insertion de la personne allocataire du RSA.
            Ce projet doit être élaboré à partir des ressources et des choix de la personne, avec l'appui du 
            référent RSA. La finalité du projet vise l'accès ou le retour à l'emploi.
            Toutefois les étapes pour y parvenir seront adaptées au rythme et aux possibilités de chacun :
            l'accompagnement doit être adapté (en termes d'intensité et de fréquence) à la singularité de chaque 
            situation, notamment en fonction des besoins de la personne, de ses projets, de ses ressources et de 
            son profil.
            La validation du CER n'intervient qu'après accord exprès du Président du Département du Nord.</p>
            
            <h4>Quels sont les engagements de chacun ? </h4>
            
            <p><u>Les engagements de l'allocataire</u></p>
                <ul>
                    <li>Respecter les droits et devoirs;</li>
                    <li>Honorer les rendez vous;</li>
                    <li>Respecter les engagements du contrat : effectuer les démarches et participer aux actions;</li>
                    <li>Tenir son référent informé des résultats des actions engagées.</li>
                </ul>
            <p><u>Les engagements du référent</u></p>
                <ul>
                    <li>Informer l'allocataire de ses droits et devoirs;</li>
                    <li>Assurer un accompagnement personnel;</li>
                    <li>Proposer des solutions adaptées afin de favoriser la réussite de l'accompagnement;</li>
                    <li>Mobiliser les ressources territoriales adéquates;</li>
                    <li>Faire un retour d'information personnalisé.</li>
                </ul>
            <h4>Quelles conséquences en cas de non respect des engagements de l'allocataire ?</h4>
            <p>Si les engagements du contrat ne sont pas respectés ou si le contrat n'est pas renouvelé, le Président
                du Département du Nord peut décider d'appliquer une sanction, après avoir consulté une équipe 
                pluridisciplinaire.</p>

